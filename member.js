const memberList = document.getElementById("memberList");
const nameEl = document.querySelector(".detail-overlay .name");
const impressionsEl = document.querySelector(".detail-overlay .impressions");
const schoolEl = document.querySelector(".detail-overlay .school");
const descEl = document.querySelector(".detail-overlay .position");
const bgEl = document.querySelector(".detail-bg");

let currentCategory = "management";

function renderMemberList(category) {
    const list = members[category];
    memberList.innerHTML = "";

    list.forEach((m, idx) => {
        const div = document.createElement("div");
        div.className = "member-item";
        div.dataset.index = idx;
        div.innerHTML = `<img src="${m.avatar}" alt="${m.name}"><div>${m.name}</div>`;
        div.addEventListener("click", () => showMember(category, idx));
        memberList.appendChild(div);
    });
}


function showMember(category, index) {
    const m = members[category][index];
    if (!m) return;
    // 分开名字和职位
    document.querySelector(".name").textContent = m.name;
    document.querySelector(".position").textContent = m.position;

    impressionsEl.textContent = m.impressions;
    schoolEl.innerHTML = m.school.map(i => `<span>${i}</span>`).join(" ");
    bgEl.src = m.bgSrc;

    // 左侧头像状态
    document.querySelectorAll(".member-item").forEach(item => item.classList.remove("active"));
    document.querySelector(`.member-item[data-index="${index}"]`)?.classList.add("active");
}


// Tab 切换
document.querySelectorAll(".tab-btn").forEach(btn => {
    btn.addEventListener("click", () => {
        document.querySelectorAll(".tab-btn").forEach(b => b.classList.remove("active"));
        btn.classList.add("active");
        currentCategory = btn.dataset.tab;
        renderMemberList(currentCategory);
        showMember(currentCategory, 0);
    });
});

// 初始化
renderMemberList(currentCategory);
showMember(currentCategory, 0);

